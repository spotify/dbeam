package com.spotify.dbeam.options;

import org.apache.beam.sdk.options.Default;
import org.apache.beam.sdk.options.Description;
import org.apache.beam.sdk.options.PipelineOptions;
import org.apache.beam.sdk.options.Validation;

public interface OutputOptions extends PipelineOptions {

  @Description("The path for storing the output.")
  @Validation.Required
  String getOutput();

  void setOutput(String value);

  @Default.Boolean(false)
  @Description("Store only the data files in output folder, skip queries,"
          + " metrics and metadata files.")
  Boolean getDataOnly();

  void setDataOnly(Boolean value);

}
